def canonical_json_bytes(value: Any) -> bytes:
    return json.dumps(value, sort_keys=True, separators=(",", ":"), allow_nan=False).encode("utf-8")


def sha256_json(value: Any) -> str:
    return hashlib.sha256(canonical_json_bytes(value)).hexdigest()


def _as_float_array(values: Sequence[float]) -> np.ndarray:
    result = np.asarray(values, dtype=float)
    if not np.all(np.isfinite(result)):
        raise ValueError("array contains a non-finite value")
    return result


def log_target_weights(spectrum: SectorSpectrum, beta: float, kappa: float) -> np.ndarray:
    """Return log w_R = kappa log(d_R) - beta C_R."""

    spectrum.validate()
    if not math.isfinite(beta) or beta < 0.0:
        raise ValueError("beta must be finite and nonnegative")
    if not math.isfinite(kappa):
        raise ValueError("kappa must be finite")
    dimensions = _as_float_array(spectrum.dimensions)
    penalties = _as_float_array(spectrum.penalties)
    return kappa * np.log(dimensions) - beta * penalties


def target_distribution(spectrum: SectorSpectrum, beta: float, kappa: float) -> np.ndarray:
    logs = log_target_weights(spectrum, beta, kappa)
    shifted = np.exp(logs - float(np.max(logs)))
    return shifted / float(np.sum(shifted))


def complete_graph_proposal(size: int) -> np.ndarray:
    """Uniform reciprocal proposal on the complete graph without self proposals."""

    if size < 2:
        raise ValueError("proposal needs at least two states")
    proposal = np.full((size, size), 1.0 / (size - 1), dtype=float)
    np.fill_diagonal(proposal, 0.0)
    return proposal


def validate_proposal(proposal: np.ndarray, atol: float = 1e-12) -> None:
    if proposal.ndim != 2 or proposal.shape[0] != proposal.shape[1]:
        raise ValueError("proposal must be square")
    if np.any(~np.isfinite(proposal)) or np.any(proposal < 0.0):
        raise ValueError("proposal entries must be finite and nonnegative")
    if not np.allclose(proposal.sum(axis=1), 1.0, atol=atol):
        raise ValueError("proposal rows must sum to one")
    reciprocal = (proposal > 0.0) == (proposal.T > 0.0)
    if not bool(np.all(reciprocal)):
        raise ValueError("proposal support must be reciprocal")


def metropolis_acceptance(
    spectrum: SectorSpectrum,
    beta: float,
    kappa: float,
    proposal: np.ndarray | None = None,
) -> np.ndarray:
    """Return the directed Metropolis--Hastings acceptance matrix."""

    if proposal is None:
        proposal = complete_graph_proposal(spectrum.size)
    proposal = np.asarray(proposal, dtype=float)
    validate_proposal(proposal)
    if proposal.shape != (spectrum.size, spectrum.size):
        raise ValueError("proposal size does not match spectrum")

    logs = log_target_weights(spectrum, beta, kappa)
    acceptance = np.zeros_like(proposal)
    for source in range(spectrum.size):
        for target in range(spectrum.size):
            if source == target or proposal[source, target] <= 0.0:
                continue
            log_ratio = (
                logs[target]
                - logs[source]
                + math.log(proposal[target, source])
                - math.log(proposal[source, target])
            )
            acceptance[source, target] = 1.0 if log_ratio >= 0.0 else math.exp(log_ratio)
    return acceptance


def metropolis_hastings_kernel(
    spectrum: SectorSpectrum,
    beta: float,
    kappa: float,
    proposal: np.ndarray | None = None,
) -> np.ndarray:
    """Build the quotient-lumpable transition kernel with the declared target law."""

    if proposal is None:
        proposal = complete_graph_proposal(spectrum.size)
    proposal = np.asarray(proposal, dtype=float)
    acceptance = metropolis_acceptance(spectrum, beta, kappa, proposal)
    kernel = proposal * acceptance
    np.fill_diagonal(kernel, 0.0)
    diagonal = 1.0 - kernel.sum(axis=1)
    if np.any(diagonal < -1e-12):
        raise ValueError("off-diagonal transition mass exceeds one")
    np.fill_diagonal(kernel, np.maximum(diagonal, 0.0))
    return kernel


def detailed_balance_error(distribution: np.ndarray, kernel: np.ndarray) -> float:
    flow = distribution[:, None] * kernel
    return float(np.max(np.abs(flow - flow.T)))


def stationary_residual(distribution: np.ndarray, kernel: np.ndarray) -> float:
    return float(np.max(np.abs(distribution @ kernel - distribution)))


def stationary_distribution_from_kernel(kernel: np.ndarray) -> np.ndarray:
    """Recover the normalized stationary left eigenvector for receipt checking."""

    values, vectors = np.linalg.eig(np.asarray(kernel, dtype=float).T)
    index = int(np.argmin(np.abs(values - 1.0)))
    vector = np.real(vectors[:, index])
    if float(np.sum(vector)) < 0.0:
        vector = -vector
    vector = np.maximum(vector, 0.0)
    total = float(np.sum(vector))
    if total <= 0.0:
        raise ValueError("stationary eigenvector is not positive")
    return vector / total


def absolute_spectral_gap(kernel: np.ndarray) -> float:
    eigenvalues = np.linalg.eigvals(np.asarray(kernel, dtype=float))
    magnitudes = sorted((float(abs(value)) for value in eigenvalues), reverse=True)
    if len(magnitudes) < 2:
        return 1.0
    return max(0.0, 1.0 - magnitudes[1])


def transition_kl_rate(
    true_distribution: np.ndarray,
    true_kernel: np.ndarray,
    alternative_kernel: np.ndarray,
) -> float:
    """Stationary one-step KL rate D(K_true || K_alt)."""

    total = 0.0
    for state, weight in enumerate(true_distribution):
        for target, probability in enumerate(true_kernel[state]):
            if probability <= 0.0:
                continue
            alternative = float(alternative_kernel[state, target])
            if alternative <= 0.0:
                return math.inf
            total += float(weight) * float(probability) * math.log(float(probability) / alternative)
    return float(total)


def simulate_transition_counts(
    kernel: np.ndarray,
    shots_per_source: int,
    seed: int,
) -> np.ndarray:
    """Generate a deterministic multinomial validation receipt, not hardware data."""

    if shots_per_source <= 0:
        raise ValueError("shots_per_source must be positive")
    rng = np.random.default_rng(seed)
    return np.vstack(
        [rng.multinomial(shots_per_source, np.asarray(row, dtype=float)) for row in kernel]
    ).astype(int)


def transition_log_likelihood(counts: np.ndarray, kernel: np.ndarray) -> float:
    """Conditional transition log likelihood, omitting model-common constants."""

    counts = np.asarray(counts, dtype=int)
    kernel = np.asarray(kernel, dtype=float)
    if counts.shape != kernel.shape:
        raise ValueError("counts and kernel must have the same shape")
    if np.any(counts < 0):
        raise ValueError("counts must be nonnegative")
    positive_counts = counts > 0
    if np.any((kernel <= 0.0) & positive_counts):
        return -math.inf
    return float(np.sum(counts[positive_counts] * np.log(kernel[positive_counts])))


def acceptance_log_likelihood(
    accepted: np.ndarray,
    totals: np.ndarray,
    acceptance: np.ndarray,
    assignment_matrix: np.ndarray | None = None,
) -> float:
    """Binomial log likelihood for directed repair accept/reject records.

    Diagonal cells and cells with zero trials are ignored.  A two-by-two
    assignment matrix may be supplied after it is frozen from matched readback
    calibration.  Rows are true bits and columns are observed bits.
    """

    accepted = np.asarray(accepted, dtype=int)
    totals = np.asarray(totals, dtype=int)
    probabilities = np.asarray(acceptance, dtype=float)
    if accepted.shape != totals.shape or accepted.shape != probabilities.shape:
        raise ValueError("accepted, totals, and acceptance must have the same shape")
    if np.any(accepted < 0) or np.any(totals < accepted):
        raise ValueError("acceptance counts must satisfy 0 <= accepted <= totals")
    if assignment_matrix is not None:
        matrix = np.asarray(assignment_matrix, dtype=float)
        if matrix.shape != (2, 2) or np.any(matrix < 0.0):
            raise ValueError("assignment_matrix must be a nonnegative 2x2 matrix")
        if not np.allclose(matrix.sum(axis=1), 1.0, atol=1e-12):
            raise ValueError("assignment_matrix rows must sum to one")
        probabilities = (1.0 - probabilities) * matrix[0, 1] + probabilities * matrix[1, 1]

    score = 0.0
    for source in range(totals.shape[0]):
        for target in range(totals.shape[1]):
            trials = int(totals[source, target])
            if source == target or trials == 0:
                continue
            successes = int(accepted[source, target])
            probability = float(probabilities[source, target])
            failures = trials - successes
            if successes and probability <= 0.0:
                return -math.inf
            if failures and probability >= 1.0:
                return -math.inf
            if successes:
                score += successes * math.log(probability)
            if failures:
                score += failures * math.log1p(-probability)
    return float(score)


def compare_acceptance_hypotheses(
    spectrum: SectorSpectrum,
    beta: float,
    accepted: np.ndarray,
    totals: np.ndarray,
    kappas: Iterable[float] = (0.0, 1.0, 2.0),
    assignment_matrix: np.ndarray | None = None,
) -> dict[str, Any]:
    """Compare dimension exponents from the instrument's public readback bits."""

    scores: dict[str, float] = {}
    for kappa in kappas:
        acceptance = metropolis_acceptance(spectrum, beta, float(kappa))
        scores[f"kappa_{float(kappa):g}"] = acceptance_log_likelihood(
            accepted,
            totals,
            acceptance,
            assignment_matrix=assignment_matrix,
        )
    oph_score = scores[f"kappa_{OPH_KAPPA:g}"]
    null_score = max(scores[f"kappa_{value:g}"] for value in NULL_KAPPAS)
    return {
        "log_likelihoods": scores,
        "oph_log_bayes_factor_vs_best_equal_prior_null": oph_score - null_score,
        "winner": max(scores, key=scores.get),
    }


def compare_kappa_hypotheses(
    spectrum: SectorSpectrum,
    beta: float,
    counts: np.ndarray,
    kappas: Iterable[float] = (0.0, 1.0, 2.0),
) -> dict[str, Any]:
    """Compare frozen dimension-exponent hypotheses with equal prior odds."""

    scores: dict[str, float] = {}
    for kappa in kappas:
        kernel = metropolis_hastings_kernel(spectrum, beta, float(kappa))
        scores[f"kappa_{float(kappa):g}"] = transition_log_likelihood(counts, kernel)
    oph_score = scores[f"kappa_{OPH_KAPPA:g}"]
    null_score = max(scores[f"kappa_{value:g}"] for value in NULL_KAPPAS)
    return {
        "log_likelihoods": scores,
        "oph_log_bayes_factor_vs_best_equal_prior_null": oph_score - null_score,
        "winner": max(scores, key=scores.get),
    }


def permute_spectrum(spectrum: SectorSpectrum, permutation: Sequence[int]) -> SectorSpectrum:
    """Relabel sectors while preserving the semantic dimension/penalty pairs."""

    if sorted(permutation) != list(range(spectrum.size)):
        raise ValueError("permutation must contain every sector index exactly once")
    return SectorSpectrum(
        name=f"{spectrum.name}__permuted",
        family=spectrum.family,
        labels=tuple(spectrum.labels[index] for index in permutation),
        dimensions=tuple(spectrum.dimensions[index] for index in permutation),
        penalties=tuple(spectrum.penalties[index] for index in permutation),
        provenance=spectrum.provenance,
        role=spectrum.role,
    )


def kernel_certificate(
    spectrum: SectorSpectrum,
    beta: float = DEFAULT_BETA,
    kappa: float = OPH_KAPPA,
) -> dict[str, Any]:
    distribution = target_distribution(spectrum, beta, kappa)
    proposal = complete_graph_proposal(spectrum.size)
    acceptance = metropolis_acceptance(spectrum, beta, kappa, proposal)
    kernel = metropolis_hastings_kernel(spectrum, beta, kappa, proposal)
    recovered = stationary_distribution_from_kernel(kernel)
    return {
        "schema_version": SCHEMA_VERSION,
        "spectrum": asdict(spectrum),
        "beta": beta,
        "kappa": kappa,
        "target_distribution": distribution.tolist(),
        "proposal": proposal.tolist(),
        "acceptance": acceptance.tolist(),
        "kernel": kernel.tolist(),
        "checks": {
            "max_row_sum_error": float(np.max(np.abs(kernel.sum(axis=1) - 1.0))),
            "minimum_entry": float(np.min(kernel)),
            "detailed_balance_error": detailed_balance_error(distribution, kernel),
            "stationary_residual": stationary_residual(distribution, kernel),
            "recovered_stationary_max_error": float(np.max(np.abs(recovered - distribution))),
            "absolute_spectral_gap": absolute_spectral_gap(kernel),
        },
    }


def identifiability_certificate(
    spectrum: SectorSpectrum,
    beta: float = DEFAULT_BETA,
) -> dict[str, Any]:
    kernels = {
        kappa: metropolis_hastings_kernel(spectrum, beta, kappa)
        for kappa in (0.0, 1.0, 2.0)
    }
    distribution = target_distribution(spectrum, beta, OPH_KAPPA)
    kl_vs_unweighted = transition_kl_rate(distribution, kernels[1.0], kernels[0.0])
    kl_vs_plancherel = transition_kl_rate(distribution, kernels[1.0], kernels[2.0])
    unequal_dimension_edge_exists = len(set(spectrum.dimensions)) > 1
    identifiable = bool(
        unequal_dimension_edge_exists
        and kl_vs_unweighted > 1e-12
        and kl_vs_plancherel > 1e-12
    )
    return {
        "spectrum": spectrum.name,
        "beta": beta,
        "unequal_dimension_edge_exists": unequal_dimension_edge_exists,
        "kl_rate_oph_vs_unweighted": kl_vs_unweighted,
        "kl_rate_oph_vs_plancherel": kl_vs_plancherel,
        "dimension_exponent_identifiable": identifiable,
        "expected_control_behavior": (
            "positive separation" if unequal_dimension_edge_exists else "zero separation by construction"
        ),
    }


def build_validation_bundle(
    beta: float,
    shots_per_source: int,
    seed: int,
) -> dict[str, Any]:
    spectra = builtin_spectra()
    rows = {}
    for offset, (name, spectrum) in enumerate(sorted(spectra.items())):
        kernel = metropolis_hastings_kernel(spectrum, beta, OPH_KAPPA)
        counts = simulate_transition_counts(kernel, shots_per_source, seed + offset)
        rows[name] = {
            "kernel_certificate": kernel_certificate(spectrum, beta, OPH_KAPPA),
            "identifiability": identifiability_certificate(spectrum, beta),
            "simulated_counts": counts.tolist(),
            "hypothesis_comparison": compare_kappa_hypotheses(spectrum, beta, counts),
        }
    bundle = {
        "schema_version": SCHEMA_VERSION,
        "receipt_class": "simulator_validation_only_not_hardware_evidence",
        "claim_boundary": (
            "The kernel validates a declared finite-sector sampler. It does not show that OPH "
            "selects the target law or that ordinary quantum mechanics is violated."
        ),
        "beta": beta,
        "shots_per_source": shots_per_source,
        "seed": seed,
        "models": {
            "oph": "kappa=1, one quotient-visible dimension factor",
            "unweighted_null": "kappa=0",
            "plancherel_full_block_null": "kappa=2",
        },
        "spectra": rows,
    }
    bundle["bundle_sha256"] = sha256_json(bundle)
    return bundle


def _json_default(value: Any) -> Any:
    if isinstance(value, np.generic):
        return value.item()
    raise TypeError(f"Object of type {type(value).__name__} is not JSON serializable")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Validate the finite-sector OPH generative repair kernel without hardware submission."
    )
    parser.add_argument("--beta", type=float, default=DEFAULT_BETA)
    parser.add_argument("--shots-per-source", type=int, default=16384)
    parser.add_argument("--seed", type=int, default=50920260711)
    parser.add_argument("--json-out", type=Path)
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    bundle = build_validation_bundle(args.beta, args.shots_per_source, args.seed)
    rendered = json.dumps(bundle, indent=2, sort_keys=True, default=_json_default) + "\n"
    if args.json_out is not None:
        args.json_out.parent.mkdir(parents=True, exist_ok=True)
        args.json_out.write_text(rendered, encoding="utf-8")
    print(rendered, end="")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
