#!/usr/bin/env python3
"""Finite-sector generative repair kernel for the blinded IBM benchmark.

This module instantiates the quotient-lumpable Metropolis--Hastings kernel
already stated in the OPH finite-quotient ensemble theorem surface.  It does
not promote that sampler to a physical law.  The target measure is an explicit
experimental hypothesis, and the dimension exponent ``kappa`` makes the
OPH reduced-edge law distinguishable from two frozen alternatives.

The module is deliberately independent of Qiskit.  It supplies the pure
mathematics, identifiability checks, likelihood calculation, deterministic
simulation, and stable receipts used by the hardware-facing protocol.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import math
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Any, Iterable, Mapping, Sequence

import numpy as np


SCHEMA_VERSION = "oph.generative-repair-kernel.v1"
DEFAULT_BETA = 0.60
OPH_KAPPA = 1.0
NULL_KAPPAS = (0.0, 2.0)


@dataclass(frozen=True)
class SectorSpectrum:
    """A finite observer-visible sector spectrum.

    ``dimensions`` are quotient-visible degeneracies.  ``penalties`` are the
    declared finite-group Casimir values or preregistered decoy penalties.
    Nothing in this class claims that repair selects the resulting law.
    """

    name: str
    family: str
    labels: tuple[str, ...]
    dimensions: tuple[int, ...]
    penalties: tuple[float, ...]
    provenance: str
    role: str

    def validate(self) -> None:
        size = len(self.labels)
        if size < 2:
            raise ValueError(f"{self.name}: at least two sectors are required")
        if len(self.dimensions) != size or len(self.penalties) != size:
            raise ValueError(f"{self.name}: labels, dimensions, and penalties must align")
        if len(set(self.labels)) != size:
            raise ValueError(f"{self.name}: sector labels must be unique")
        if any(int(value) != value or value <= 0 for value in self.dimensions):
            raise ValueError(f"{self.name}: dimensions must be positive integers")
        if any(not math.isfinite(value) or value < 0.0 for value in self.penalties):
            raise ValueError(f"{self.name}: penalties must be finite and nonnegative")
        if min(self.penalties) != 0.0:
            raise ValueError(f"{self.name}: one declared zero mode is required")

    @property
    def size(self) -> int:
        return len(self.labels)


