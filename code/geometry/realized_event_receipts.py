def realized_records(stages: int = 3, n_germs: int = 20, seed: int = 7):
    """One germ per stage-0 record cell (E1 at the seeding scale), followed
    through refinement; chart noise calibrated to the measured cross-ratio
    residuals; clock = 2pi-normalized modular unit per tick."""
    rng = np.random.default_rng(seed)
    tower = tower_meshes(stages)
    base_records, base_coords = tower[0]
    n_germs = min(n_germs, len(base_records))
    germs = []
    for token in range(n_germs):
        pos0 = centroid_on_sphere(base_records[token], base_coords)
        tau0 = float(token)  # one commit per tick at the seeding pass
        chain = []
        for stage in range(stages):
            noise = MEASURED_CR_RELATIVE_ERROR[min(stage, 2)]
            radius = mesh_scale(stage) * 0.5 + 3.0 * noise
            jitter = rng.normal(scale=noise, size=3)
            pos = pos0 + jitter
            pos = pos / np.linalg.norm(pos)
            chain.append({
                "stage": stage,
                "position": pos,
                "tau": tau0 + float(rng.normal(scale=noise)),
                "radius": radius,
                "certified": True,
            })
        germs.append({"token": token, "chain": chain})
    return germs


