def e1_population_receipt(germs, stages: int = 3) -> dict:
    radii = [[g["chain"][s]["radius"] for g in germs] for s in range(stages)]
    max_radii = [max(r) for r in radii]
    return {
        "n_germs": len(germs),
        "seeding_scale": mesh_scale(0),
        "max_radius_per_stage": max_radii,
        "radii_shrinking": all(max_radii[i] > max_radii[i + 1]
                               for i in range(stages - 1)),
        "one_germ_per_stage0_cell": len(germs) == 20,
    }


def e2_separation_receipt(germs) -> dict:
    """Certified disjoint boxes at the finest stage for every germ pair."""
    worst_margin = np.inf
    n = len(germs)
    separated = 0
    for i in range(n):
        for j in range(i + 1, n):
            a, b = germs[i]["chain"][-1], germs[j]["chain"][-1]
            gap = np.linalg.norm(a["position"] - b["position"])
            margin = gap - (a["radius"] + b["radius"])
            worst_margin = min(worst_margin, margin)
            if margin > 0 and a["certified"] and b["certified"]:
                separated += 1
    total_pairs = n * (n - 1) // 2
    return {
        "pairs": total_pairs,
        "certified_separated_pairs": separated,
        "worst_margin": float(worst_margin),
        "all_separated": separated == total_pairs,
    }


def e4_cocycle_receipt(germs) -> dict:
    """Transition between the two produced charts (north/south stereographic)
    must be the Moebius inversion w = z/|z|^2 up to the calibrated chart
    noise, with residual decreasing along refinement."""
    residuals = []
    stages = len(germs[0]["chain"])
    for s in range(stages):
        errs = []
        for g in germs:
            p = g["chain"][s]["position"]
            if abs(p[2]) > 0.8:
                continue  # keep away from both poles
            z = stereographic_chart(p, +1)
            w = stereographic_chart(p, -1)
            w_pred = z / (z @ z)  # the exact Moebius transition
            w_pred[1] = w_pred[1]
            errs.append(np.linalg.norm(w - w_pred * np.array([1.0, 1.0])))
        residuals.append(float(np.median(errs)))
    return {
        "median_transition_residual_per_stage": residuals,
        "cocycle_at_noise_level": all(r < 5.0 * MEASURED_CR_RELATIVE_ERROR[
            min(s, 2)] for s, r in enumerate(residuals)),
    }


def one_ring_step(records, coords) -> float:
    """Mean geodesic distance between patch-star-adjacent record centroids:
    the lattice propagation scale (one write-set per tick)."""
    cents = [centroid_on_sphere(r, coords) for r in records]
    dists = []
    for a in range(0, len(records), 17):
        for b in range(len(records)):
            if a != b and set(records[a]) & set(records[b]):
                dists.append(np.arccos(np.clip(cents[a] @ cents[b], -1, 1)))
    return float(np.mean(dists))


def cone_structure_receipt(n_ticks: int = 50, per_tick: int = 3,
                           stage: int = 2, max_dtick: int = 25,
                           seed: int = 11) -> dict:
    """Fit a quadratic cone separating intrinsic ancestry pairs from
    non-ancestry pairs, with the clock calibrated so the lattice
    propagation speed is one (tau in units of the one-ring crossing time)
    and displacements taken in the tangent frame of the earlier event.
    Expected: Lorentzian signature (1,2) with the timelike eigenvector
    along the clock axis. The cone interior is underfilled (a causal pair
    needs an actual chain of intermediate commits), so the classification
    rate is bounded away from one; the signature is the receipt."""
    rng = np.random.default_rng(seed)
    tower = tower_meshes(stage + 1)
    records, coords = tower[stage]
    cents = [centroid_on_sphere(r, coords) for r in records]
    step = one_ring_step(records, coords)

    patch_star = {}
    for idx, rec in enumerate(records):
        for pch in rec:
            patch_star.setdefault(pch, set()).add(idx)
    commits = []
    for tick in range(n_ticks):
        for _ in range(per_tick):
            ri = int(rng.integers(len(records)))
            flipped = max(records[ri])
            write = {pch for r in patch_star[flipped] for pch in records[r]}
            commits.append({"tick": tick, "record": ri,
                            "read": set(records[ri]) | write,
                            "write": write})
    n = len(commits)
    depends = np.zeros((n, n), dtype=bool)
    for j in range(n):
        for i in range(j):
            if (commits[j]["tick"] > commits[i]["tick"]
                    and commits[j]["read"] & commits[i]["write"]):
                depends[j, i] = True
    for k in range(n):
        for j in range(n):
            if depends[j, k]:
                depends[j] |= depends[k]

    rows, targets = [], []
    for j in range(n):
        p_j = cents[commits[j]["record"]]
        for i in range(j):
            dtick = commits[j]["tick"] - commits[i]["tick"]
            if dtick == 0 or dtick > max_dtick:
                continue
            p_i = cents[commits[i]["record"]]
            dist = float(np.arccos(np.clip(p_i @ p_j, -1, 1)))
            ref = (np.array([0.0, 0.0, 1.0]) if abs(p_i[2]) < 0.9
                   else np.array([1.0, 0.0, 0.0]))
            e_1 = np.cross(p_i, ref)
            e_1 /= np.linalg.norm(e_1)
            e_2 = np.cross(p_i, e_1)
            tangent = p_j - (p_i @ p_j) * p_i
            t_norm = np.linalg.norm(tangent)
            ang = (0.0 if t_norm < 1e-12
                   else np.arctan2(tangent / t_norm @ e_2,
                                   tangent / t_norm @ e_1))
            v = np.array([dtick * step,
                          dist * np.cos(ang), dist * np.sin(ang)])
            v /= np.linalg.norm(v)
            outer = np.outer(v, v)
            rows.append(outer[np.triu_indices(3)]
                        * (2.0 - np.eye(3))[np.triu_indices(3)])
            targets.append(-1.0 if depends[j, i] else 1.0)
    rows_a, targets_a = np.array(rows), np.array(targets)
    coef, *_ = np.linalg.lstsq(rows_a, targets_a, rcond=None)
    q = np.zeros((3, 3))
    q[np.triu_indices(3)] = coef
    q = (q + q.T) / 2.0
    evals, evecs = np.linalg.eigh(q)
    scale = abs(evals).max()
    neg = int(np.sum(evals < -1e-6 * scale))
    pos = int(np.sum(evals > 1e-6 * scale))
    time_axis_alignment = float(abs(evecs[0, 0]))
    pred = np.where(rows_a @ coef < 0, -1.0, 1.0)
    return {
        "n_commits": n,
        "one_ring_step": step,
        "labelled_pairs": len(targets_a),
        "causal_fraction": float(np.mean(targets_a < 0)),
        "signature": [neg, pos],
        "timelike_eigenvector_clock_alignment": time_axis_alignment,
        "classification_rate": float(np.mean(pred == targets_a)),
    }


def pca_dimension_receipt(germs) -> dict:
    """The realized event sheet must measure as (1+2)-dimensional: the
    machinery detects boundary-only population instead of certifying a
    fictitious fourth dimension."""
    pts = []
    for g in germs:
        last = g["chain"][-1]
        uv = stereographic_chart(last["position"], +1)
        pts.append([last["tau"], uv[0], uv[1]])
    pts = np.array(pts)
    centered = pts - pts.mean(axis=0)
    svals = np.linalg.svd(centered, compute_uv=False)
    dim = int(np.sum(svals / svals[0] > 1e-2))
    return {"chart_pca_dimension": dim, "expected_screen_sheet_dim": 3}


def instrument_realized_events(stages: int = 3) -> dict:
    germs = realized_records(stages=stages)
    e1 = e1_population_receipt(germs, stages)
    e2 = e2_separation_receipt(germs)
    e4 = e4_cocycle_receipt(germs)
    cone = cone_structure_receipt()
    pca = pca_dimension_receipt(germs)
    witnessed = {
        "e1_screen_population": bool(e1["radii_shrinking"]
                                     and e1["one_germ_per_stage0_cell"]),
        "e2_certified_separation": bool(e2["all_separated"]),
        "e4_moebius_cocycle": bool(e4["cocycle_at_noise_level"]),
        "intrinsic_cone_lorentzian_1p2": bool(
            cone["signature"] == [1, 2]
            and cone["classification_rate"] > 0.75
            and cone["timelike_eigenvector_clock_alignment"] > 0.9
        ),
        "realized_sheet_dimension_measured": bool(
            pca["chart_pca_dimension"] == 3
        ),
        "e3_rank_four_bulk_depth": False,  # the honest negative: no bulk
        # depth channel exists on the realized tower yet
    }
    return {
        "artifact": "oph_realized_event_receipts",
        "object_id": "RealizedEventReceipts_Issue503",
        "issue": 503,
        "scope": (
            "records generated by the sequential transactional repair "
            "dynamics of the cyclic cap-net tower; charts are the produced "
            "stereographic embeddings with noise calibrated to the measured "
            "modular cross-ratio residuals; clock is the 2pi-normalized "
            "modular unit per commit tick; ancestry is the intrinsic "
            "read-after-write transitive closure. The realized event set is "
            "a (1+2)-dimensional screen sheet, measured as such --- exactly "
            "countermodel (i) of the event packet: dimension is a "
            "population output. The bulk-depth record channel (E3 rank "
            "four, hence 3+1) is the remaining structural gate."
        ),
        "e1": e1,
        "e2": e2,
        "e4": e4,
        "cone_structure": cone,
        "pca": pca,
        "receipts_witnessed": witnessed,
        "receipts_pending": [
            "E3 rank-four frames: bulk-depth record channel (H^3 radial "
            "coordinate) on the realized tower",
            "bulk E1 population (records off the screen sheet)",
        ],
    }


def main() -> None:
    report = instrument_realized_events()
    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(REPORT_PATH, "w") as f:
        json.dump(report, f, indent=2, default=float)
        f.write("\n")
    print(json.dumps(report["receipts_witnessed"], indent=2))
    print(json.dumps(report["cone_structure"], indent=2))


if __name__ == "__main__":
    main()
