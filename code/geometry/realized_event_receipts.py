# chart-noise calibration from the witnessed modular cross-ratio residuals
# (code/geometry/runs/modular_clock_instrumentation_report.json)
MEASURED_CR_RELATIVE_ERROR = {0: 1.14e-2, 1: 2.3e-3, 2: 2.3e-3 / 2.0}


# ---------------------------------------------------------------------------
# realized tower geometry
# ---------------------------------------------------------------------------

def tower_meshes(stages: int = 3):
    """Records and unit-sphere vertex coordinates per stage."""
    tris, coords = icosahedron()
    tower = [(list(tris), np.array(coords))]
    for _ in range(stages - 1):
        finer, _ = refinement_subdivide(tower[-1][0])
        verts = sorted({p for rec in finer for p in rec})
        old_coords = tower[-1][1]
        new_coords = np.zeros((max(verts) + 1, 3))
        new_coords[: len(old_coords)] = old_coords
        # midpoints projected to the sphere (same construction the producer
        # receipts validated via cross-ratio production)
        k_old = tower[-1][0]
        edges = sorted({tuple(sorted(e)) for rec in k_old
                        for e in [(rec[0], rec[1]), (rec[1], rec[2]),
                                  (rec[0], rec[2])]})
        next_label = len(old_coords)
        for a, b in edges:
            mid = old_coords[a] + old_coords[b]
            new_coords[next_label] = mid / np.linalg.norm(mid)
            next_label += 1
        tower.append((finer, new_coords))
    return tower


def centroid_on_sphere(rec, coords) -> np.ndarray:
    c = coords[list(rec)].mean(axis=0)
    return c / np.linalg.norm(c)


def stereographic_chart(p: np.ndarray, pole: int = +1) -> np.ndarray:
    """Chart from the pole (+1: north, -1: south)."""
    x, y, z = p
    denom = 1.0 - pole * z
    if abs(denom) < 1e-9:
        denom = 1e-9
    return np.array([x / denom, y / denom])


def mesh_scale(stage: int) -> float:
    return 1.1 / (2.0 ** stage)  # icosahedral edge ~1.05, halved per stage


# ---------------------------------------------------------------------------
# sequential transactional repair with ancestry tracking
# ---------------------------------------------------------------------------

def sequential_repair_run(records, n_ticks: int, seed: int = 20260712):
    """At each tick, seed one conflict at a pseudo-random record, repair
    transactionally, and track read/write patch sets. Returns per-tick
    commits and the intrinsic ancestry relation (transitive closure of
    read-after-write)."""
    rng = np.random.default_rng(seed)
    patch_star = {}
    for idx, rec in enumerate(records):
        for p in rec:
            patch_star.setdefault(p, set()).add(idx)
    commits = []
    for tick in range(n_ticks):
        rec_idx = int(rng.integers(len(records)))
        system = RepairSystem(records, seed_record=rec_idx)
        nf = system.repair()
        assert not RepairSystem.conflicted_records(
            type("S", (), {"records": records, "state": nf})()
        )
        flipped_patch = max(records[rec_idx])
        write_set = {p for r in patch_star[flipped_patch]
                     for p in records[r]}
        read_set = set(records[rec_idx]) | write_set
        commits.append({"tick": tick, "record": rec_idx,
                        "read": read_set, "write": write_set})
    # direct dependence: read-after-write
    n = len(commits)
    depends = np.zeros((n, n), dtype=bool)
    for j in range(n):
        for i in range(j):
            if commits[j]["read"] & commits[i]["write"]:
                depends[j, i] = True
    # transitive closure (ancestry)
    for k in range(n):
        for j in range(n):
            if depends[j, k]:
                depends[j] |= depends[k]
    return commits, depends


